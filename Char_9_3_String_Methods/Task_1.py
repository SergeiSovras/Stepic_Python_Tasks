s = input()
print(s.capitalize()) # Заменяет первую букву на строчную
print(s.swapcase()) # Регистр символа меняется на противоположный
print(s.title()) # Первый символ каждого слова переводится в верхний регистр
print(s.lower()) # Все символы в нижнем регистре
print(s.upper()) # Все символы в верхнем регистре

